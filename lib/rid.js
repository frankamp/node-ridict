buildREs = function(facets) {
	var REs = {};
	Object.keys(facets).forEach(function (key) {
		var newRE = facets[key].join("|");
		newRE = "(" + newRE.replace(new RegExp("\\*", 'g'),".*?\\b") + ")";
		REs[key] = new RegExp(newRE,"i");
	});
	return REs;
};

exports.facet_matchers = buildREs(exports.facets);

exports.matches = function(input){
	var result = {};
	Object.keys(exports.facet_matchers).forEach(function (key) {
		var re = new RegExp(exports.facet_matchers[key].source, 'gi');
		var match_list;
		result[key] = 0;
		while ((match_list = re.exec(input)) !== null){
			result[key]++;
		}
	});
	return result;
};
